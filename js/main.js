function alternarGabarito1() {
    var gabarito = document.getElementById("gabarito1");
    var botao = document.getElementById("botao1");

    if (gabarito.style.display === "none") {
        gabarito.style.display = "block";
        botao.textContent = "Esconder Resposta";
        botao.style.backgroundColor = "red"; 
    } else {
        gabarito.style.display = "none";
        botao.textContent = "Resposta";
        botao.style.backgroundColor = "#1E2A78"; 
    }
}

